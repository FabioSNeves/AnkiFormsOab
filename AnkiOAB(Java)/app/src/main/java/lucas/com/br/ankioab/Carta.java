public class Carta {

    public Integer CodCarta ;
    public Integer CodBaralho ;
    public String Frente ;
    public String Verso ;

    public Integer getCodCarta() {return CodCarta;}

    public void setCodCarta(Integer codCarta) {CodCarta = codCarta;}

    public Integer getCodBaralho() {return CodBaralho;}

    public void setCodBaralho(Integer codBaralho) {CodBaralho = codBaralho;}

    public String getFrente() {return Frente;}

    public void setFrente(String frente) {Frente = frente;}

    public String getVerso() {return Verso;}

    public void setVerso(String verso) {Verso = verso;}

}
